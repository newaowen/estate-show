package com.warsong.app.estateshow.model;

public class LocationModel {

	private String titleShow;
	
	private String subTitleShow;
	
	private String longitude;
	
	private String Latitude;
	
	private String address;

	public String getTitleShow() {
		return titleShow;
	}

	public void setTitleShow(String titleShow) {
		this.titleShow = titleShow;
	}

	public String getSubTitleShow() {
		return subTitleShow;
	}

	public void setSubTitleShow(String subTitleShow) {
		this.subTitleShow = subTitleShow;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return Latitude;
	}

	public void setLatitude(String latitude) {
		this.Latitude = latitude;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
	
}
