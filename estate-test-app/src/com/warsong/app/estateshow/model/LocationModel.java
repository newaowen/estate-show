package com.warsong.app.estateshow.model;

public class LocationModel {

	private String titleShow;
	
	private String subTitleShow;
	
	private String longitude;
	
	private String latitude;

	public String getTitleShow() {
		return titleShow;
	}

	public void setTitleShow(String titleShow) {
		this.titleShow = titleShow;
	}

	public String getSubTitleShow() {
		return subTitleShow;
	}

	public void setSubTitleShow(String subTitleShow) {
		this.subTitleShow = subTitleShow;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
}
